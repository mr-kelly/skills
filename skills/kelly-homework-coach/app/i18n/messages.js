export function resolveLanguage() {
  const forced = new URLSearchParams(window.location.search).get("lang") || "";
  if (forced.toLowerCase().startsWith("zh")) return "zh";
  if (forced.toLowerCase().startsWith("en")) return "en";
  const saved = localStorage.getItem("khc-language") || "auto";
  if (saved === "en" || saved === "zh") return saved;
  return navigator.languages?.some((language) => language.toLowerCase().startsWith("zh")) ? "zh" : "en";
}
